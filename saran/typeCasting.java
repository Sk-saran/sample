package Assignment;

public class typeCasting {

	public static void main(String[] args) {

			        String num = "15915";
			        System.out.println("Original string value: "+num);
			        int value1 = Integer.parseInt(num);
			        System.out.println("int: "+value1);
			        long value2 = Long.parseLong(num);
			        System.out.println("long: "+value2);
			        float value3 = Float.parseFloat(num);
			        System.out.println("float: "+value3);
			        double value4 = Double.parseDouble(num);
			        System.out.println("double:" +value4);


	}


}